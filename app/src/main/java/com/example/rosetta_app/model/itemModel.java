package com.example.rosetta_app.model;

public class itemModel {
    private int translatorImg;
    private String translatorName;
    private String translatorDescription;
    private String translatorRating;

    public int getTranslatorImg() {
        return translatorImg;
    }

    public void setTranslatorImg(int translatorImg) {
        this.translatorImg = translatorImg;
    }

    public String getTranslatorName() {
        return translatorName;
    }

    public void setTranslatorName(String translatorName) {
        this.translatorName = translatorName;
    }

    public String getTranslatorDescription() {
        return translatorDescription;
    }

    public void setTranslatorDescription(String translatorDescription) {
        this.translatorDescription = translatorDescription;
    }

    public String getTranslatorRating() {
        return translatorRating;
    }

    public void setTranslatorRating(String translatorRating) {
        this.translatorRating = translatorRating;
    }
}
